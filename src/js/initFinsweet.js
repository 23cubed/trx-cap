function initFinsweet() {
    window.fsAttributes = window.fsAttributes || [];
    // fs-list
    window.fsAttributes.push(['fs-list']);
    // fs-readtime
    window.fsAttributes.push(['fs-readtime']);
    // fs-inject
    window.fsAttributes.push(['fs-inject']);
    // fs-socialshare
    window.fsAttributes.push(['fs-socialshare']);
    // fs-copyclip
    window.fsAttributes.push(['fs-copyclip']);
    // fs-toc
    window.fsAttributes.push(['fs-toc']);
    // fs-scrolldisable
    window.fsAttributes.push(['fs-scrolldisable']);
  
  }
  
  export { initFinsweet };
  